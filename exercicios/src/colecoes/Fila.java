package colecoes;

import java.util.LinkedList;
import java.util.Queue;

public class Fila {

	public static void main(String[] args) {
		
		Queue<String> fila = new LinkedList<>();
		
		// Offer e Add -> adicionam elementos na Fila
		// Diferença é o comportamento
		// quando a fila está cheia!
		fila.add("Ana"); // retorna false
		fila.offer("Bia"); // lança exception
		fila.add("Carlos");
		fila.offer("Daniel");
		fila.add("Rafaela");
		fila.offer("Gui");
		
		// Peek e Element -> obter o proximo elemento da fila sem remover
		// Diferença é o comportamento
		// quando a fila está vazia!
		System.out.println(fila.peek()); // retorna null
		System.out.println(fila.peek());
		System.out.println(fila.element()); // Lança uma exception
		System.out.println(fila.element());
		
		// Pool e Remove -> obtem o proximo elemento da fila e remove!
		// Diferença é o comportamento
		// quando a fila está vazia!
		System.out.println(fila.poll()); // retorna false
		System.out.println(fila.remove()); // retorna uma exception
		System.out.println(fila.poll());
		System.out.println(fila.poll());
		System.out.println(fila.poll());
		System.out.println(fila.poll());
		System.out.println(fila.remove());
		
		// fila.size();
		// fila.clear();
		// fila.isEmpty();
		// fila.contains();
	}
}
